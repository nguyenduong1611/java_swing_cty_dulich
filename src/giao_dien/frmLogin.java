    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "select * from NhanVien "
                    + "where manv = ? and matkhau = ?";
            PreparedStatement pstm = conn.prepareStatement(sql);
            String username = txtTK.getText();
            String password = new String(txtMK.getPassword());

            pstm.setString(1, username);
            pstm.setString(2, password);

            rs = pstm.executeQuery();
            String vaitro = "";
            while (rs.next()) {
                vaitro = rs.getString("machucvu");
            }
            pstm.close();
            rs.close();

            if (vaitro.equalsIgnoreCase("")) {
                JOptionPane.showMessageDialog(this, "User hoặc Pass chưa đúng");
            } else {
                if (vaitro.equalsIgnoreCase("ql")) {
                    frmMenu menu = new frmMenu();
                    JOptionPane.showMessageDialog(this, "Đăng nhập với quyền quản lý thành công");
                    menu.setVisible(true);

                } else if (vaitro.equalsIgnoreCase("nvvp")) {
                    frmMenu menu = new frmMenu();
                    JOptionPane.showMessageDialog(this, "Đăng nhập với quyền nhân viên thành công");
                    menu.setVisible(true);
                    menu.btnTrangChu.setEnabled(true);
                    menu.btnKhachHang.setVisible(true);
                    menu.btnHoaDon.setVisible(true);
                    menu.btnTourDuLich.setVisible(true);
                    menu.btnCaLamViec.setVisible(false);
                    menu.btnDiaDanh.setVisible(true);
                    menu.btnNhanVien.setVisible(false);
                    menu.btnChucVu.setVisible(false);

                } else {
                    JOptionPane.showMessageDialog(this,"Bạn không có quyền đăng nhập vào hệ thống ");
                }
                this.dispose(); //đóng form login

            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại!" + e.toString());
        }


    }//GEN-LAST:event_btnDangNhapActionPerformed

