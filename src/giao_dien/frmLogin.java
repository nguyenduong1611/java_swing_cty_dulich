    public void loadDataToList() {
        try {
            String sql = "select * from NhanVien";
            st = conn.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                String taikhoan = rs.getString("manv");
                String matkhau = rs.getString("matkhau");
                String chucVu = rs.getString("machucvu");
                list.add(new NhanVien(taikhoan, matkhau, chucVu));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load data to list");
        }
    }

    public boolean checkLogin(String TaiKhoan, String MatKhau) {
        for (NhanVien nv : list) {
            if (nv.getManv().equalsIgnoreCase(TaiKhoan) && nv.getMatkhau().equalsIgnoreCase(MatKhau)) {
                return true;
            }
        }
        return false;
    }

